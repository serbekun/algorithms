      program FUNCTON;
      integer,parameter :: ihuha=10000;
      real*8 :: st, per1, per2, per3, per4, x, a1, a2, a3;
      integer :: i;
      real*8, dimension(0:ihuha) :: fu, xx;
      
      per1=60.4; per3=18.1; per3=32.05; per4=91.0;
      a1=1.0
      a1=1.0; a2=3.0; a3=42.0;
      
      do i=0,ihuha; st=1.0/ihuha;
         xx(i)=st*i; x=xx(i);
         fu(i) = SIN(per1*x)+SIN(per2*x) - a2*SIN(per3*x) + a4*SIN(per4*x);
      enddo;
    
      open(unit=4,file='aaa.dat')
      do i=1,ihuha;
          write(4,*)xx(i),fu(i);
      enddo;    
      close(4);
      
      end program FUNCTON;
